/* Bass Tutor — practice-routine builder. Assembles a session from:
   bass/data/exercise-index.json (generated by npm run build:bass), the
   practice log (bass-tutor:practice via BTPractice), and page progress
   (BassProgress). Never-practiced exercises from pages you've touched come
   first, then the stalest; a warmup and an ear-training block bookend it.  */
(function () {
  "use strict";

  const MINUTES_PER_EXERCISE = 4;

  document.addEventListener("DOMContentLoaded", async () => {
    const $ = (id) => document.getElementById(id);
    if (!$("rb-build")) return;

    const list = $("rb-list"), status = $("rb-status");
    let exercises = null;

    try {
      const res = await fetch("../data/exercise-index.json");
      if (!res.ok) throw new Error(res.status);
      exercises = await res.json();
    } catch {
      status.innerHTML = "Couldn't load the exercise index — if you opened this " +
        "file directly, start the dev server (<code>npm run dev</code>). " +
        "Meanwhile: tune up, run one technique drill, then play along with the " +
        "<a href='groove-machine.html'>groove machine</a> for ten minutes.";
      $("rb-build").disabled = true;
      return;
    }

    function build() {
      const minutes = Number(document.querySelector("[name=rb-time]:checked")?.value || 20);
      const log = window.BTPractice ? window.BTPractice.all() : {};
      const progress = window.BassProgress ? window.BassProgress.all() : {};
      const touchedPages = new Set(Object.keys(progress));

      const scored = exercises.map((ex) => {
        const rec = log[ex.id];
        const pageId = ex.page.split("/").pop().replace(".html", "");
        const pageTouched = touchedPages.has(pageId);
        const daysSince = rec ? (Date.now() - new Date(rec.last).getTime()) / 86400000 : Infinity;
        // prefer: exercises from pages you've studied, never/least-recently practiced
        const score = (pageTouched ? 1000 : 0) + Math.min(daysSince, 365);
        return { ex, rec, pageTouched, daysSince, score };
      }).sort((a, b) => b.score - a.score);

      const slots = Math.max(2, Math.floor((minutes - 6) / MINUTES_PER_EXERCISE));
      const chosen = scored.slice(0, slots);

      list.replaceChildren();
      let n = 0;

      function addItem(title, body, minutesLabel) {
        n++;
        const card = document.createElement("section");
        card.className = "practice-card";
        card.innerHTML = `<h3>${n}. ${title} <span class="tempo-chip">${minutesLabel}</span></h3>${body}`;
        list.appendChild(card);
        return card;
      }

      addItem("Tune up", "<p class='desc'>Every session, every time — it's also ear training. " +
        "<a href='tuner.html'>Tuner</a> or by ear from the reference tones.</p>", "2 min");
      addItem("Warmup: 1-2-3-4 permutations",
        "<p class='desc'>One finger per fret, all four strings, with the metronome — " +
        "<a href='../04-technique/left-hand.html#practice-left-hand-permutations'>the drill</a>.</p>", "4 min");

      for (const item of chosen) {
        const when = !item.rec ? "never practiced"
          : item.daysSince < 1 ? "practiced today"
          : `last practiced ${Math.floor(item.daysSince)} day${item.daysSince >= 2 ? "s" : ""} ago`;
        const tempo = item.ex.tempo ? ` · ♩ ${item.ex.tempo[0]}${item.ex.tempo[1] ? "→" + item.ex.tempo[1] : ""}` : "";
        addItem(item.ex.title,
          `<p class="desc">${item.ex.goal || ""}</p>
           <p class="log-note">${item.ex.unit} · ${when}${tempo} —
           <a href="../${item.ex.page}#practice-${item.ex.id}">open the exercise →</a></p>`,
          `${MINUTES_PER_EXERCISE} min`);
      }

      addItem("Ears or music (your pick)",
        "<p class='desc'>Finish with the <a href='ear-trainer.html'>ear trainer</a> " +
        "or play something you love along with the <a href='groove-machine.html'>groove machine</a>. " +
        "The fun five minutes are load-bearing.</p>", "5 min");

      status.textContent = `${minutes}-minute session · ${chosen.length} exercises from your path — ` +
        (Object.keys(progress).length
          ? "picked by staleness from pages you've studied."
          : "you haven't completed any pages yet, so this starts from the top of the course.");
      $("rb-print").hidden = false;
    }

    $("rb-build").addEventListener("click", build);
    $("rb-print").addEventListener("click", () => window.print());
  });
})();
